package com.tallerwebi.dominio;

import java.util.List;

public class Ronda {
    private List<Usuario> usuarios;
    private List<Carta> cartas;

    public Ronda(List<Usuario> usuarios, List<Carta> cartas) {
        this.usuarios = usuarios;
        this.cartas = cartas;
    }
    public Ronda() {
        this.usuarios = usuarios;
        this.cartas = cartas;
    }



}
